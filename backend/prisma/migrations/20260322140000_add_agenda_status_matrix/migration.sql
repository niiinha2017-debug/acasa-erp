    WHEN UPPER(COALESCE(`categoria`, '')) IN ('AGENDAR_MEDIDA_FINA', 'MEDIDA_FINA') THEN 'MEDIDA_FINA'
    WHEN UPPER(COALESCE(`categoria`, '')) IN ('AGUARDANDO_PROJETO_TECNICO', 'PROJETO_TECNICO_EM_ANDAMENTO', 'PROJETO_TECNICO_CONCLUIDO', 'PROJETO_TECNICO_APROVADO') THEN 'PROJETO_TECNICO'
    WHEN UPPER(COALESCE(`categoria`, '')) IN ('PRODUCAO_RECEBIDA', 'PRODUCAO_EM_ANDAMENTO', 'PRODUCAO_FINALIZADA', 'PREPARACAO_TECNICA', 'CORTE', 'MONTAGEM_INTERNA', 'ACABAMENTO', 'CONFERENCIA_QUALIDADE') THEN 'PRODUCAO'
    WHEN UPPER(COALESCE(`categoria`, '')) IN ('AGENDAR_ENTREGA', 'ENTREGA_AGENDADA', 'EM_ENTREGA', 'ENTREGA_FINALIZADA') THEN 'ENTREGA'
    WHEN UPPER(COALESCE(`categoria`, '')) IN ('AGENDAR_MONTAGEM', 'MONTAGEM_AGENDADA', 'MONTAGEM_CLIENTE_AGENDADA', 'EM_MONTAGEM', 'EM_MONTAGEM_CLIENTE', 'MONTAGEM_FINALIZADA', 'MONTAGEM_CLIENTE_FINALIZADA') THEN 'MONTAGEM'
    WHEN UPPER(COALESCE(`categoria`, '')) = 'GARANTIA' THEN 'GARANTIA'
    WHEN UPPER(COALESCE(`categoria`, '')) = 'ASSISTENCIA' THEN 'ASSISTENCIA'
    WHEN UPPER(COALESCE(`categoria`, '')) IN ('MANUTENCAO', 'AGENDAR_POS_VENDA') THEN 'MANUTENCAO'
    ELSE 'PRODUCAO'
  END,
  `execucao_etapa` = CASE
    WHEN UPPER(COALESCE(`status`, '')) = 'CONCLUIDO' THEN 'CONCLUIDO'
    WHEN UPPER(COALESCE(`status`, '')) = 'EM_PRODUCAO' THEN 'EM_ANDAMENTO'
    WHEN UPPER(COALESCE(`status`, '')) = 'PAUSADO' THEN 'EM_ANDAMENTO'
    WHEN UPPER(COALESCE(`status`, '')) = 'EM_ANDAMENTO' AND UPPER(COALESCE(`categoria`, '')) IN ('AGENDAR_MEDIDA', 'AGENDAR_APRESENTACAO', 'AGENDAR_MEDIDA_FINA', 'MEDIDA_FINA', 'AGUARDANDO_PROJETO_TECNICO', 'MONTAGEM_CLIENTE_AGENDADA', 'AGENDAR_POS_VENDA', 'AGENDAR_ENTREGA', 'ENTREGA_AGENDADA', 'AGENDAR_MONTAGEM', 'MONTAGEM_AGENDADA') THEN 'AGENDADO'
    WHEN UPPER(COALESCE(`status`, '')) = 'EM_ANDAMENTO' THEN 'EM_ANDAMENTO'
    WHEN UPPER(COALESCE(`status`, '')) = 'PENDENTE' THEN 'PENDENTE'
    ELSE 'PENDENTE'
  END;