    WHEN UPPER(COALESCE(`categoria`, '')) IN ('AGENDAR_ENTREGA', 'ENTREGA_AGENDADA', 'EM_ENTREGA', 'ENTREGA_FINALIZADA') THEN 'LOGISTICA'
    WHEN UPPER(COALESCE(`categoria`, '')) IN ('AGENDAR_MONTAGEM', 'MONTAGEM_AGENDADA', 'MONTAGEM_CLIENTE_AGENDADA', 'EM_MONTAGEM', 'EM_MONTAGEM_CLIENTE', 'MONTAGEM_FINALIZADA', 'MONTAGEM_CLIENTE_FINALIZADA') THEN 'LOGISTICA'
